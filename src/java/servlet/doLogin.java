    /**
     * Handles the HTTP
     * <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        String remember = request.getParameter("remember");
        User u = db.helpers.dbUser.getUser(username, password);
        if (u != null && u.getRole().equals("Administrator")) {
            if ("remember".equals(remember)) {
                Cookie c = new Cookie("username", username);
                c.setMaxAge(60 * 20);
                response.addCookie(c);
                c = new Cookie("password", password);
                c.setMaxAge(60 * 20);
                response.addCookie(c);
            }
            HttpSession sesija = request.getSession(true);
            sesija.setAttribute("User", u);
            RequestDispatcher dp = request.getRequestDispatcher("AdminPanel.jsp");
            dp.forward(request, response);
        } else {
            RequestDispatcher dp = request.getRequestDispatcher("AdminLogin.jsp");
            dp.forward(request, response);
        }
    }

